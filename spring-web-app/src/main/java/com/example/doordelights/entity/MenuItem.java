
package com.example.doordelights.entity;

public class MenuItem {

	String menuItemId;
	String menuItemName;
	String menuItemDescription;
	String menuItemPhoto;
	String menuItemType;
	
	public String getMenuItemDescription() {
		return menuItemDescription;
	}
	public void setMenuItemDescription(String menuItemDescription) {
		this.menuItemDescription = menuItemDescription;
	}
	public String getMenuItemId() {
		return menuItemId;
	}
	public void setMenuItemId(String menuItemId) {
		this.menuItemId = menuItemId;
	}
	public String getMenuItemName() {
		return menuItemName;
	}
	public void setMenuItemName(String menuItemName) {
		this.menuItemName = menuItemName;
	}
	public String getMenuItemPhoto() {
		return menuItemPhoto;
	}
	public void setMenuItemPhoto(String menuItemPhoto) {
		this.menuItemPhoto = menuItemPhoto;
	}
	public String getMenuItemType() {
		return menuItemType;
	}
	public void setMenuItemType(String menuItemType) {
		this.menuItemType = menuItemType;
	}
	
	
}